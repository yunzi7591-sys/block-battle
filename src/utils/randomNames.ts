/**
 * randomNames.ts — AI対戦用ランダム日本人名リスト
 */

const AI_NAMES: string[] = [
    'かむ', 'にく', 'たろう', 'ひな', 'ゆう',
    'りく', 'そら', 'はる', 'みお', 'あおい',
    'れん', 'さくら', 'こはる', 'ひろ', 'ゆい',
    'けん', 'まお', 'りん', 'かい', 'なな',
    'しゅう', 'あかり', 'つむぎ', 'いつき', 'ほのか',
    'だいち', 'すず', 'みなと', 'ここ', 'ひかる',
    'たくみ', 'えま', 'あさひ', 'めい', 'ゆうと',
    'ふう', 'のあ', 'しおり', 'かなた', 'るい',
];

/**
 * ランダムな日本人風名前を返す
 */
export function getRandomAIName(): string {
    return AI_NAMES[Math.floor(Math.random() * AI_NAMES.length)];
}
